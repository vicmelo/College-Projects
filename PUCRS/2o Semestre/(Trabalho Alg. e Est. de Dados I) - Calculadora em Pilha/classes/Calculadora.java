
public class Calculadora {
	
	public static float soma(float n1, float n2) {
        
        return n1 + n2;
        
    }
    
    public static float mult(float n1, float n2) {
        
        return n1 * n2;
        
    }
    
    public static float sub(float n1, float n2) {
        return n1 - n2;
    }
    
    public static float div(float n1, float n2) {
        return n1/n2;
        
    }
    
    public static float cos(float n1) {
        
        return (float)Math.cos(n1);
        
    }
    
    public static float sin(float n1) {
        
        return (float)Math.sin(n1);
        
    }
    
    public static float atan(float n1, float n2) {
        
        return (float)Math.atan2(n1, n2);
        
    }
}
